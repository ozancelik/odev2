package odev2;

public class Course {
	
	public Course(int id, String name, String price, String detail, String lecturer) {
		this.id = id;
		this.name = name;
		this.price = price;
		this.detail = detail;
		this.lecturer = lecturer;
	}
	
	int id;
	String name;
	String price;
	String detail;
	String lecturer;
}
